package Bean;

/**
 *
 * @author sunny
 */
public class ScheduleBean {
    private int id;
    private String email;
    private String day;
    private String time;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getDay() {
        return day;
    }

    public void setDay(String day) {
        this.day = day;
    }

    public String getTime() {
        return time;
    }

    public void setTime(String time) {
        this.time = time;
    }

    @Override
    public String toString() {
        return "ScheduleBean{" + "id=" + id + ", email=" + email + ", day=" + day + ", time=" + time + '}';
    }

    public ScheduleBean() {
    }
    
    
    
    
}
